代数方程组求解:
        对于方程ax=？b，a 为an×m矩阵，有三种情况：
         当n=m时，此方程成为“恰定”方程
                恰定方程组的解
                x=inv(a)*b — 采用求逆运算解方程  
                x=a\b  —  采用左除运算解方程      
                    
         当n>m时，此方程成为“超定”方程
                超定方程组的解: 方程多 变量少
                    x=(a' a)-1 a ' b      —— 求逆法           
                    x=a\b  matlab用最小二乘法找一个准确地基本解。     
                    
         当n<m时，此方程成为“欠定”方程
                无穷多个解
插值与拟合：
        插值：由实验或测量得到函数y=f(x)一系列离散的点，利用这些点建
                立变量之间的多项式关系。
        插值多项式：拉格朗日插值多项式、牛顿插值多项式

        拟合：指已知某函数的若干离散函数值（f1,f2,…,fn），通过调整该函
                数中若干待定系数，使得该函数与已知点集合的差别最小。
        线性拟合、非线性拟合、样条拟合

        插值与拟合区别：插值要求必须通过离散点，拟合不必。
        如果得到的数据点比较精确，通常用插值方法。
        若数据中的误差较大，用拟合。

    一维插值:当被插值函数为一元函数时，为一维插值
            Vq=interp1(X,V,Xq,method)
                X：自变量的取值范围
                V：函数值，或一个向量，长度与X一致
                Xq：插值点向量
                method：设定插值方法
                    ‘nearest’、‘linear’ 、‘spline’ 、‘pchip’ 
                    ‘cubic’ 、‘v5cubic’
        分段线性插值（linear）
            x=0:2*pi
            y = sin(x)
            xx = 0:0.5:2*pi
            yy = interp1(x,y,xx)
        一维快速傅里叶插值：interpft():
            该函数用傅里叶变换到频域，然后用更多的点的傅里叶逆变换
                变回时域，其结果是对数据进行增采样。
                y=interpft(x,n,dim)
                
                x=0:1.2:10
                y = sin(x)
                n = 2*length(x)
                yi = interpft(y,n)
                xi = 0:0.6:10.4
    拟合:
        p=ployfit(X,Y,N)
            X,Y为离散点的坐标值，N是N次插值，返回一个多项式，p为输出
                向量，其分量为求得的多项式的系数。
    二维插值:
        Vq=interp2(X,Y,V,Xq,Yq,method)
            X,Y,V：为维数相同的矩阵，
            V：是数据点（X,Y）上的函数值，
            Xq，Yq：为待插值数据网格
            method：设定插值方法
            ‘nearest’、‘linear’ 、‘spline’ 、‘pchip’ 、
            ‘cubic’ 、‘v5cubic’
