        根据精度的不同，有cumsum（矩形积分）、trapz（梯形积分）、
            quad（辛普森积分）
        
        矩形积分：
            cumsum（x，dim）
                x为向量或矩阵，dim为求积方向
        梯形积分： trapz（x,y,dim）:
            
