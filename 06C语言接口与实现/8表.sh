关联表：
        是一组键-值对的集合 它很想数组 只是索引的可以是任何类型值
        例如： 编译器要维护符号表。。该表将名称映射到名称的属性集合

        和原子不一样    
            原子 直接把字符串或则整形数 存到一个内存中 返回地址
            键值由hash函数给定 hash不同 键值不同
        关联表：
            数据结构内有 关键字和数据 
            存储的时候      存储关键字和数据

接口：
        在 接口中 不会写数据结构
        在 实现中 定义数据结构和函数。。。
