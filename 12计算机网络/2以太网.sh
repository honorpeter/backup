网桥与冲突域:
            将一个总线型的网络 分为多个小的总线性网络 使用网桥连接
            这样可以减小 冲突域

            网桥中有一张转发表: 查看MAC地址是否在每个端口中
            怎么建立转发表: 每次 终端通信的时候 更新原MAC和对应的端口号
                            没有项 添加
                            设置计时器 定时删除没有发送数据的端口号和终端

            网桥: 一般就是 集线器


