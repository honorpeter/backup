Internet接入技术：
        PSDN: 
                通过电话线的方式接入
                通过分配一个 号码进行接入
                通过拨号进行接入 终端和接入控制设备之间建立点对点语音信道
                    这个语音信道是一直存在的    PSTN造成严重的负担
                终端上网和拨打电话两者只能二选一
        ADSL：  
                也是通过电话线的方式接入 可以同时上网和打电话
                关键通过两个设备 ADSL Model 和DSLAM
        以太网：
                接入网是以太网
                终端通过以太网接入到控制设备
                接入速度是10Mbps到100Mbps
        无线路由：
                接入网还是以太网 
                终端通过无线路由器连接到以太网
                这种方式通过无线路由器解决了家庭移动终端接入问题
                接入速度是10Mbps到100Mbps


