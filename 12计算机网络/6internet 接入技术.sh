        无线路由器的功能和使用方法：
            从组成上 无线路由器相当于三个设备：
                第一 一个多端口的交换机：
                    用来连接家庭当中的安装以太网网卡的台式机
                第二 相当于有一个AP：
                    AP通过无线信道和家庭笔记本电脑或者智能手机等
                第三 相当有一个路由器：
                    把路由器拆分为三部分：
                        通过一个WAN端口 与接入网相连 通过一个LAN端口与
                            交换机相连  LAN是看不到的虚拟的
                        交换机连接的是 以太网
                            由交换机实现AP 路由器以及装有以太网网卡的台式机
                        AP 连接的无线局域网 
                            由AP实现以太网和无线局域网这样两个网络之间的互连

       如果家庭范围较大      可以采用无线局域网那讲中介绍的AP-repeater模式
             在家庭当中电磁波覆盖的范围内   增加一个AP1

无线路由器怎么样连接到Internet当中：
                静态IP地址接入方式：
                    无线路由器的WAN端口要进行手工配置IP地址
                            子网掩码和默认网关
                    这种方式 不需要身份鉴别
                DHCP接入方式
                PPPoE接入方式：
                    对无线路由器配置从ISP获取的用户名和口令
                    接入控制设备需要对无线路由器进行身份鉴别
                    从接入控制设备地址池选择一个IP地址 分配给路由器
        以太网终端接入方式或者以太网家庭局域网接入方式下
                终端和无线路由器都可以采用静态IP地址 PPPoE方式
        对于ADSL终端接入方式    和ADSL家庭局域网接入方式:
                终端和无线路由器一般只能采用PPPoE方式

私有IP地址：
