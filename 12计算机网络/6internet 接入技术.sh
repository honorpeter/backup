        终端和Internet的数据传输过程当中
                内部私有的IP地址是不能进入到互联网中去 
                路由器实现 私有IP地址和全球IP之间的转换：简称为NAT

        NAT的过程:
            终端的网络号和无线路由器LAN端口的网络号要相同
            无线路由器的LAN端口的IP地址作为默认网关地址

            终端访问web服务器：
                终端A把数据封装成一个IP分组
                    IP分组当中的目的IP地址是web服务器的地址 
                    源IP地址是它自己的IP地址
                然后把这样一个IP分组通过AP发送给路由器
                    对IP地址进行转换
                        目的IP地址还是web服务器的IP地址
                        源IP地址就换成了无线路由器的WAN端口的IP地址

                        当收到数据 传输给终端A的时候：
                            传输给终端A的时候 又使用私有的IP地址
            路由器通过 终端连接路由器的端口号 分别每个终端
            
                    











