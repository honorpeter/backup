系统启动规范：
        BIOS： 固化到计算机主板上的程序
            BIOS-MBR: 上面所说的内容 ：只能有四个分区 64字节 每个16
            BIOS-GPT： 可以描述 更多的分区
            PXE：       可以从网络启动 在BIOS加入网络协议栈
        UEFI:  同一可扩展固件接口
            

