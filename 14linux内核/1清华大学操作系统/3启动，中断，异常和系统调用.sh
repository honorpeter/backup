        3 主引导程序： 去哪个扇区 读取程序操作系统 
                因为：现在电脑一般都有多个分区 每个分区上有不同的操作系统
                结束标志： 55AA
                        
            格式： MBR
                一共512字节 一个扇区 446存放启动代码  剩下的字节存放 
                (66字节) 硬盘分区表    每个分区的状态 
                启动代码： 446kb
                        检查分区表正确性
                        加载并跳转到硬盘的引导程序
                硬盘分区表：64kb
                        描述分区状态位置
                        每个分区描述信息占据16kb
            分区引导扇区格式： 
                    文件卷头： 文件系统描述信息
                    结束标志： 55AA
                    跳转指令： 跳转到启动代码 
                          指令和     与平台相关 
                    启动代码： 跳转到加载程序 bootloader

        4 将加载程序(bootloader) 从磁盘的引导扇区 (512字节) 加到0x7c00
            跳转到CS：IP = 0000:7c00 
            控制权交给加载程序 由加载程序 加载操作系统
