<cmath>头文件:
    #include<cmath>

ceil (x)　　　　　　 x取整为不小于x的最小整数值
cos(x) 　　　　　　 x(x弧度)的余弦值
fabs(x) 　　　　　　x的绝对值
floor(x) 　　　　　　x取整为不大于x的最大整数值
fmod(x,y) 　　　　 x/y的浮点余数值
log(x)　　　　　 　 x(底数为e)的自然对数值
log10(x) 　　　　   x(底数为10)的自然对数值
pow(x,y) 　　　　  x的y次幂（x^y）的值
sin(x) 　　　　　　  x(弧度)的正弦值
sqrt(x) 　　　　　　x的平方根的值
tan(x)　　　　　　   x(弧度)的正切值
exp(x)　　　　　　   指数函数e^x的值
acos(x) 　　　　　　x(弧度)的反余弦值
asin(x) 　　　　　　 x(弧度)的反正弦值
atan(x) 　　　　　　 x(弧度)的反正切值
tanh(x) 　　　　　　 x(弧度)的双曲正切值
cosh(x) 　　　　 　　x(弧度)的双曲余弦值
sinh(x) 　　　　　　  x(弧度)的双曲正弦值
ldexp(y,x) 　　　　　返回y*2^x的值
atan2(y,x) 　　　　  返回反正切的表达式y / x 
frexp(y,&x) 　　　　 把一个浮点数分解为尾数和指数, y要分解的浮点数据,x存放指数 返回值为尾数,公式:其中 y(浮点数) = 返回值(尾数) * 2^x(指数)
modf(y,&x) 　　　　分解y,以得到y的整数(返回值)和小数部分(放在x参数中)









