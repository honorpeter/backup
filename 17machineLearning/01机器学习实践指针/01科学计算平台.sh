目前计算机处理较多的数学计算主要分为
    1 数值计算 以数值数组为运算对象 给出数值解 计算过程中可能会产生累计误差
         影响精确性  计算速度快 占用资源少
    
    2 符号计算  以符号对象和符号表达式为计算对象 给出解析解 运算不受误差累计
        计算指令简单 占用资源多 耗时G长

计算软件:
    Matlab
        数值计算 可视化编程......  money
    Octave 
        和Matlab相似    数值求解 线性和非线性问题   free
    Mathematica:
        数学系统  符号演算 数值计算 图形
    Maple
        强有力的符号计算能力 高精度的数字计算 灵活的图形显示和高效的编程
    SPSS
        统计分析 数据和文本挖掘 预测模型 和 决策优化
    R
        R语言用于 统计分析 绘图的语言
        也可以用做矩阵计算
    Numpy Scipy matplotlib 等python科学计算平台
        


    其他软件:
