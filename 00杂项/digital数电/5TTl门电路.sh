TTL门电路:
    输入级:
        二极管与门
        二极管或门

        单发射级输入
                二极管起保护作用
        多发射级输入

    中间级形式:

        单变量分相器
        多变量分相器

    输出形式电路:
        图腾柱
        集电极开路
        三态输出门
        

几种典型的TTL逻辑门电路:
        将上面的输入级 中间级 输出级 组合在一起便可以构成了
    TTL反相器   非门
    TTL 与非门
    TTL或非门

    











